#!/usr/bin/env node
/*
 * Generates browser/runtime manifests for Harthmere carried-equipment animations.
 * The source CSV is produced by the asset-generation step and only references files
 * that came from the supplied art pack. Keep this generator deterministic so future
 * asset updates are reviewable in git diffs.
 */
const fs = require("fs");
const path = require("path");

const projectRoot = process.argv[2] || process.cwd();
const publicRoot = path.join(projectRoot, "public/assets/harthmere/equipment_animations");
const csvPath = path.join(publicRoot, "equipment_animation_manifest.csv");
const jsonPath = path.join(publicRoot, "equipment-animation-manifest.json");
const tsPath = path.join(
  projectRoot,
  "src/shared/game/medieval/harthmereEquipmentAnimationManifest.generated.ts"
);

function parseCsvLine(line) {
  const out = [];
  let cur = "";
  let quoted = false;
  for (let i = 0; i < line.length; i += 1) {
    const ch = line[i];
    if (ch === '"') {
      if (quoted && line[i + 1] === '"') {
        cur += '"';
        i += 1;
      } else {
        quoted = !quoted;
      }
    } else if (ch === "," && !quoted) {
      out.push(cur);
      cur = "";
    } else {
      cur += ch;
    }
  }
  out.push(cur);
  return out;
}

function parseCsv(text) {
  const lines = text.replace(/\r\n/g, "\n").replace(/\r/g, "\n").split("\n").filter(Boolean);
  const headers = parseCsvLine(lines.shift());
  return lines.map((line) => {
    const cells = parseCsvLine(line);
    const row = {};
    headers.forEach((h, i) => {
      row[h] = cells[i] ?? "";
    });
    return row;
  });
}

function stripAnimatedPrefix(p) {
  return p.replace(/^animated_gltf\//, "animated_gltf/");
}

if (!fs.existsSync(csvPath)) {
  throw new Error(`Missing CSV manifest: ${csvPath}`);
}

const rows = parseCsv(fs.readFileSync(csvPath, "utf8"));
const entries = rows.map((row) => {
  const relativeGltf = stripAnimatedPrefix(row.output_gltf);
  const fsPath = path.join(publicRoot, relativeGltf);
  if (!fs.existsSync(fsPath)) {
    throw new Error(`Manifest references missing GLTF: ${relativeGltf}`);
  }
  const animations = row.animations.split(",").map((s) => s.trim()).filter(Boolean);
  const frameCount = Number(row.frame_count || 0);
  if (frameCount !== 24) {
    throw new Error(`Expected 24 frames for ${row.name}, got ${row.frame_count}`);
  }
  if (!animations.every((name) => name.endsWith("_24"))) {
    throw new Error(`Expected all animation names to end with _24 for ${row.name}`);
  }
  return {
    id: row.name,
    category: row.category,
    slot: row.slot,
    sourceKind: row.source_kind,
    sourcePath: row.source_path,
    relativeGltf,
    assetUrl: `/assets/harthmere/equipment_animations/${relativeGltf}`,
    animations,
    frameCount,
    fps: 24,
  };
});

entries.sort((a, b) => a.category.localeCompare(b.category) || a.id.localeCompare(b.id));
const byId = Object.fromEntries(entries.map((entry) => [entry.id, entry]));
const categories = [...new Set(entries.map((entry) => entry.category))].sort();
const categoryCounts = Object.fromEntries(
  categories.map((category) => [category, entries.filter((entry) => entry.category === category).length])
);
const manifest = {
  generatedAt: new Date().toISOString(),
  description:
    "Harthmere carried-equipment animation manifest. All assets are generated from provided source art; each clip has exactly 24 frames at 24 FPS.",
  publicRoot: "/assets/harthmere/equipment_animations",
  total: entries.length,
  categories,
  categoryCounts,
  entries,
  byId,
};

fs.writeFileSync(jsonPath, `${JSON.stringify(manifest, null, 2)}\n`);

const ts = `/* eslint-disable */
/*
 * Auto-generated by scripts/harthmere/generate-equipment-animation-manifest.cjs.
 * Do not hand-edit. Regenerate after changing files under public/assets/harthmere/equipment_animations.
 *
 * These are carried-equipment object animations, not full character skeletal animations.
 * Runtime code should attach the GLTF object to the player/NPC hand, back, belt, or shield anchor,
 * then play one of the listed 24-frame clips.
 */

export type HarthmereEquipmentAnimationCategory =
  | "accessories"
  | "magic"
  | "ranged"
  | "shields"
  | "weapons"
  | "wearables";

export interface HarthmereEquipmentAnimationManifestEntry {
  id: string;
  category: HarthmereEquipmentAnimationCategory;
  slot: string;
  sourceKind: string;
  sourcePath: string;
  relativeGltf: string;
  assetUrl: string;
  animations: readonly string[];
  frameCount: 24;
  fps: 24;
}

export const HARTHMERE_EQUIPMENT_ANIMATION_PUBLIC_ROOT = "/assets/harthmere/equipment_animations" as const;

export const HARTHMERE_EQUIPMENT_ANIMATION_MANIFEST = ${JSON.stringify(entries, null, 2)} as const satisfies readonly HarthmereEquipmentAnimationManifestEntry[];

export const HARTHMERE_EQUIPMENT_ANIMATION_BY_ID = Object.fromEntries(
  HARTHMERE_EQUIPMENT_ANIMATION_MANIFEST.map((entry) => [entry.id, entry])
) as unknown as Record<string, HarthmereEquipmentAnimationManifestEntry>;

export function getHarthmereEquipmentAnimation(id: string) {
  return HARTHMERE_EQUIPMENT_ANIMATION_BY_ID[id];
}

export function getHarthmereEquipmentAnimationsByCategory(
  category: HarthmereEquipmentAnimationCategory
) {
  return HARTHMERE_EQUIPMENT_ANIMATION_MANIFEST.filter((entry) => entry.category === category);
}
`;
fs.writeFileSync(tsPath, ts);

console.log(`Generated ${entries.length} equipment animation manifest entries.`);
console.log(`JSON: ${path.relative(projectRoot, jsonPath)}`);
console.log(`TS:   ${path.relative(projectRoot, tsPath)}`);
console.log(`Counts: ${JSON.stringify(categoryCounts)}`);
